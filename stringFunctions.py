string = "Hello World"

print("String:\"Hello World\"")
print("Converted into uppercase:", string.upper())
print("Check if it is in uppercase.:", string.isupper())
print("Converted into lowercase:", string.lower())
print("Check if its in lowercase.:", string.islower())
print("Check if it contains space.:", string.isspace())
print("Length of string:", len(string))
print("Check if its alphabetic:", string.isalpha())
print("Check if it contains only digits:", string.isdigit())
print("Converted into title case:", string.title())
print("Capitalized:", string.capitalize())
print("Replace World with India:", string.replace("World", "India"))

